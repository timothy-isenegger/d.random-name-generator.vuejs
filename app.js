let nameGeneratorComponent = {
  template: '#nameGeneratorTemplate',
  data() {
    return {
      name: {
        firstname: '',
        lastname: '',
      },
      gender: '',
      race: '',
      races: [
        {
          name: 'Dwarf',
          value: 'dwarf'
        },
        {
          name: 'Elf',
          value: 'elf'
        },
        {
          name: 'Dragonborn',
          value: 'dragonborn'
        },
        {
          name: 'Gnome',
          value: 'gnome'
        },
      ],
      femaleFirstNames: FEMALE_FIRST_NAMES,
      maleFirstNames: MALE_FIRST_NAMES,
      familyNames: FAMILY_NAMES,
    }
  },
  methods: {
    getName() {
      if (this.gender === '' || this.race === '') {
        window.alert('Please select a gender and a race!');
        return false;
      }

      this.name.firstname = this.getFirstNameByGenderAndRace();
      this.name.lastname = this.getLastNameByGenderAndRace();
    },
    getFirstNameByGenderAndRace() {
      const arrayToBeFiltered = this.gender === 'male' ? this.maleFirstNames : this.femaleFirstNames;

      const filteredNames = arrayToBeFiltered.filter((item) => {
        return item.race.toLocaleLowerCase() === this.race.toLocaleLowerCase();
      });

      const randomNumber = this.getRandomNumber(filteredNames.length);
      return filteredNames[randomNumber].name;
    },
    getLastNameByGenderAndRace() {
      const filteredNames = this.familyNames.filter((item) => {
        return item.race.toLocaleLowerCase() === this.race.toLocaleLowerCase();
      });

      const randomNumber = this.getRandomNumber(filteredNames.length);
      return filteredNames[randomNumber].name;
    },
    getRandomNumber(maxValue) {
      return Math.floor(Math.random() * maxValue);
    },
  },
  computed: {
    nameGenerated: function() {
      return this.name.firstname !== '';
    }
  }
}

new Vue({
  el: '#app',
  components: {
    'name-generator': nameGeneratorComponent
  }
});
